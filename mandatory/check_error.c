void	empty_arg(int ac, char **av)
{
	int	i;

	i = 1;
	while (i < ac)
	{
		if (ft_strlen(av[i]) == 0)
		{
			write(1, "error: empty arg!\n", 18);
			exit(1);
		}
		i++;
	}
}
